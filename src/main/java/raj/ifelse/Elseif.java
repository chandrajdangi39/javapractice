package raj.ifelse;
public class Elseif{
    public static void main(String [] args){
        int age = 56;
        if(age>56){
            System.out.println(" you are experienced! ");
        }
        else if(age>46){
            System.out.println(" you are semi-experienced! ");
        }
        else if(age>36){
            System.out.println(" you are semi-semi experienced! ");
        }
    }
}